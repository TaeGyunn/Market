package com.ho.MK.admin;

public class Admin {
	
	private String Admin_id;
	private String Admin_pw;
	private String Admin_name;
	private String Admin_reg_date;
	private String Admin_address;
	private String Admin_tel;
	
	public String getAdmin_id() {
		return Admin_id;
	}
	public void setAdmin_id(String admin_id) {
		Admin_id = admin_id;
	}
	public String getAdmin_pw() {
		return Admin_pw;
	}
	public void setAdmin_pw(String admin_pw) {
		Admin_pw = admin_pw;
	}
	public String getAdmin_name() {
		return Admin_name;
	}
	public void setAdmin_name(String admin_name) {
		Admin_name = admin_name;
	}
	public String getAdmin_reg_date() {
		return Admin_reg_date;
	}
	public void setAdmin_reg_date(String admin_reg_date) {
		Admin_reg_date = admin_reg_date;
	}
	public String getAdmin_address() {
		return Admin_address;
	}
	public void setAdmin_address(String admin_address) {
		Admin_address = admin_address;
	}
	public String getAdmin_tel() {
		return Admin_tel;
	}
	public void setAdmin_tel(String admin_tel) {
		Admin_tel = admin_tel;
	}
}
